use arbitrary_int::prelude::*;

pub trait BitWidth {
    fn width() -> usize;
    fn size() -> usize;
}

#[macro_export]
macro_rules! impl_bit_width {
    ($type_name:ident, $type_width:literal, $type_size:literal) => {
        impl BitWidth for $type_name {
            fn width() -> usize {
                $type_width
            }

            fn size() -> usize {
                $type_size
            }
        }
    };
}

